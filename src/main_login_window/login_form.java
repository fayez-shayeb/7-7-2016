public void check_program_expiration()
{
    try {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date s = sdf.parse("2013-12-1");
        java.util.Date e = sdf.parse("2014-6-1");
        Calendar start = Calendar.getInstance();
        Calendar end = Calendar.getInstance();
        Calendar nowDate = Calendar.getInstance();
        start.setTime(s);
        end.setTime(e);
        if(start.compareTo(nowDate)>0||end.compareTo(nowDate)<0)
        {
            Joptionpane_message("انتهت صلاحية البرنامج\n الرجاء الاتصال على 0599313905");
            System.exit(0);
            System.exit(1);
        }
    } catch (ParseException ex) {
        Logger.getLogger(login_form.class.getName()).log(Level.SEVERE, null, ex);
    }
}
